package input_output;

public class ToanTu3ngoi {
    public static void main(String[] args) {
        /*
         *   Expression1 = Condition ? Expression2 : Expression3
         * */
        //Format code: Ctrl + Alt + L
        int a = (10 < 5) ? 20 : 30;
        System.out.println("Giá trị a: "+a);
        /*
        * Xây dựng ứng dụng xem phim 18+, cho phép người dùng nhập tên và tuổi, nếu người dùng
        * trên 18 tuổi thì thông báo "Mời bạn xem phim", còn không thì thông báo "Bạn chưa đủ tuổi"
        * */
    }
}
